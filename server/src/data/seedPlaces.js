const normalizeText = (value = "") =>
  value
    .toString()
    .trim()
    .toLowerCase()
    .replace(/\s+/g, " ");

const mergeArrays = (a = [], b = []) =>
  [...new Set([...(a || []), ...(b || [])].filter(Boolean))];

const mergePlace = (base, incoming) => {
  const merged = { ...base };

  for (const [key, value] of Object.entries(incoming)) {
    if (Array.isArray(value)) {
      merged[key] = mergeArrays(merged[key], value);
      continue;
    }

    if (
      value !== undefined &&
      value !== null &&
      value !== "" &&
      (merged[key] === undefined ||
        merged[key] === null ||
        merged[key] === "" ||
        merged[key] === false)
    ) {
      merged[key] = value;
    }
  }

  merged.featured = Boolean(base.featured || incoming.featured);
  merged.isNewOrGrowing = Boolean(base.isNewOrGrowing || incoming.isNewOrGrowing);

  merged.popularityScore = Math.max(
    base.popularityScore || 50,
    incoming.popularityScore || 50
  );

  merged.rating = Math.max(base.rating || 4, incoming.rating || 4);

  return merged;
};

const dedupePlaces = (places) => {
  const map = new Map();

  for (const place of places) {
    const key = `${normalizeText(place.name)}__${normalizeText(place.region)}`;

    const prepared = withDefaults(place, map.size);

    prepared.slug = slugify(prepared.name);

    if (!map.has(key)) {
      map.set(key, prepared);
    } else {
      map.set(key, mergePlace(map.get(key), prepared));
    }
  }

  return Array.from(map.values()).map((place, index) => ({
    ...place,
    featuredOrder: place.featured ? index + 1 : 0,
    seoTitle: place.seoTitle || `${place.name} Travel Guide`,
    seoDescription:
      place.seoDescription ||
      place.shortDescription ||
      `Discover ${place.name}, one of Ethiopia’s curated travel places.`,
  }));
};

const seedPlaces = async () => {
  try {
    await mongoose.connect(process.env.MONGO_URI);

    const places = dedupePlaces(rawPlaces);

    await Place.deleteMany({});
    await Place.insertMany(places);

    console.log(`Seeded ${places.length} unique places successfully.`);
    process.exit();
  } catch (error) {
    console.error("Place seeding failed:", error);
  
    process.exit(1);
  }
};

seedPlaces();