const seedPlaces = async () => {
  try {
    await mongoose.connect(process.env.MONGO_URI);

    const formattedPlaces = places.map((place) => ({
      ...place,
      slug: slugify(place.name),
      lastVerifiedAt: new Date(),
    }));

    for (const place of formattedPlaces) {
      await Place.findOneAndUpdate(
        { slug: place.slug },
        place,
        {
          upsert: true,
          new: true,
          runValidators: true,
        }
      );
    }

    console.log("Places seeded/updated successfully");
    process.exit();
  } catch (error) {
    console.error(error);
    process.exit(1);
  }
};

seedPlaces();